export const getProductsByApplication = (application: string): GlassProduct[] => {
  return glassProducts.filter(p =>
    p.application.some(a => a.toLowerCase().includes(application.toLowerCase()))
  );
};

export const getProductById = (id: string): GlassProduct | undefined => {
  return glassProducts.find(p => p.id === id);
};
